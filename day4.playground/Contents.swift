//Day 4 Warmup
//Using if, else if, and else
var favDrink = "Starbucks"

if favDrink == "Starbucks" {
    print("Starbucks is pretty good!")
}
else if favDrink == "Dunkin Donuts" {
    print("Dunkin Donuts is pretty good too")
}
else {
    print("I'm not so sure about this")
}
//Practice with for in loops
var favFoods = ["pizza" , "pasta" , "ice cream" , "cookies" , "chips"]
for favFood in favFoods {
    print("I think that \(favFood) is really good")
}

